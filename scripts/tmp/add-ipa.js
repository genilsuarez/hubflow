// Read the vocabulary file
const vocabPath = path.join(__dirname, '../../data/vocabulary.js');
let content = fs.readFileSync(vocabPath, 'utf8');

// For each item, add ipa field after term if we have it in our map
let addedCount = 0;
let missedTerms = [];

for (const [term, ipa] of Object.entries(IPA_MAP)) {
  // Escape special regex chars in term
  const escaped = term.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  // Match the pattern: { term: "Term", ... } and insert ipa after term line
  // We look for `term: "X"` followed by comma and other fields
  const regex = new RegExp(
    `(\\{ term: "${escaped}",\\s*)`,
    'g'
  );
  
  const newContent = content.replace(regex, `$1ipa: "${ipa}", `);
  if (newContent !== content) {
    content = newContent;
    addedCount++;
  } else {
    missedTerms.push(term);
  }
}

fs.writeFileSync(vocabPath, content, 'utf8');
console.log(`Added IPA to ${addedCount} items.`);
if (missedTerms.length > 0) {
  console.log(`Missed ${missedTerms.length} terms (pattern mismatch):`);
  missedTerms.forEach(t => console.log(`  - ${t}`));
}
